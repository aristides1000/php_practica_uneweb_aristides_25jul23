<?php
  /*
    PHP NULL Value

    Null es un tipo de datos especial que sólo puede tener un valor: NULL.

    Una variable de tipo de datos NULL es una variable que no tiene ningún valor asignado.

    Sugerencia: si se crea una variable sin un valor, se le asigna automáticamente un valor NULL.

    Las variables también se pueden vaciar estableciendo el valor NULL:
  */

  $x = "Hola mundo!";
  $x = null;
  var_dump($x);
?>